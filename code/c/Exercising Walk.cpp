#include <iostream>
#include <cmath>
using namespace std;
int main()
{
	long long n,i;
	cin>>n;
	long long a,b,c,d,x1,x2,y1,y2,x,y;
	for(i=0;i<n;i++)
	{
		int flag=1;
		cin>>a>>b>>c>>d>>x>>y>>x1>>y1>>x2>>y2;
		if(x==x1&&x==x2&&a!=0)
		{
			cout<<"No"<<endl;
			continue;
		}
		if(x==x1&&x==x2&&b!=0)
		{
			cout<<"No"<<endl;
			continue;
		}
		if(y==y1&&y==y2&&c!=0)
		{
			cout<<"No"<<endl;
			continue;
		}
		if(y==y1&&y==y2&&d!=0)
		{
			cout<<"No"<<endl;
			continue;
		}
		if(a>b)
		{
			if(x-(a-b)>=x1)
			{
				flag=1;
			}
			else
			{
				flag=0;
				cout<<"No"<<endl;
				continue;
			}
		}
		else
		{
			if(x+(b-a)<=x2)
			{
				flag=1;
			}
			else
			{
				flag=0;
				cout<<"No"<<endl;
				continue;
			}
		}
		if(c>d)
		{
			if(y-(c-d)>=y1)
			{
				flag=1;
			}
			else
			{
				flag=0;
				cout<<"No"<<endl;
				continue;
			}
		}
		else
		{
			if(y+(d-c)<=y2)
			{
				flag=1;
			}
			else
			{
				flag=0;
				cout<<"No"<<endl;
				continue;
			}
		}
		if(flag==1)
		{
			cout<<"Yes\n";
		}
	}
	return 0;
} 
